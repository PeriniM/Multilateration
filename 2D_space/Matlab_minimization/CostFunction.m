function f0 = CostFunction(x, w1, w2, w3, w4, w5, w6)

d12 = 0.4383522782028957;
d13 = 0.7432427098009082;
d14 = 0.05358215211972959;
d23 = 0.6851613686097892;
d24 = 0.39489583683257085;
d34 = 0.6980268103479745;

x1 = 0.33116723275746407;
y1 = 0.8795259980742361;

x2 = x(1);
x3 = x(2);
x4 = x(3);
y2 = x(4);
y3 = x(5);
y4 = x(6);

eq1 = (x1-x2)^2 + (y1-y2)^2 - d12^2;
eq2 = (x1-x3)^2 + (y1-y3)^2 - d13^2;
eq3 = (x1-x4)^2 + (y1-y4)^2 - d14^2;
eq4 = (x2-x3)^2 + (y2-y3)^2 - d23^2;
eq5 = (x2-x4)^2 + (y2-y4)^2 - d24^2;
eq6 = (x3-x4)^2 + (y3-y4)^2 - d34^2;

f0 = w1*eq1^2 + w2*eq2^2 + w3*eq3^2 + w4*eq4^2 + w5*eq5^2 + w6*eq6^2;
end